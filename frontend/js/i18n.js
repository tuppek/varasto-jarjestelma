function getLang() {
  return localStorage.getItem(I18N_STORAGE_KEY) || "fi";
}

function getTheme() {
  return localStorage.getItem(THEME_STORAGE_KEY) || "light";
}

function getLocale() {
  return LOCALE_MAP[getLang()] || "fi-FI";
}

function t(key, vars) {
  const lang = getLang();
  let str = MESSAGES[lang]?.[key] ?? MESSAGES.fi[key] ?? key;
  if (vars && typeof vars === "object") {
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replaceAll(`{${k}}`, String(v));
    });
  }
  return str;
}

function setLang(lang) {
  if (!MESSAGES[lang]) return;
  localStorage.setItem(I18N_STORAGE_KEY, lang);
  document.documentElement.lang = lang;
}

function setTheme(theme) {
  const value = theme === "dark" ? "dark" : "light";
  localStorage.setItem(THEME_STORAGE_KEY, value);
  document.documentElement.setAttribute("data-theme", value);
  const meta = document.querySelector('meta[name="theme-color"]');
  if (meta) meta.content = value === "dark" ? "#111827" : "#ffffff";
}

function applyStaticI18n() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
    if (el.hasAttribute("aria-label")) el.setAttribute("aria-label", t(el.dataset.i18nTitle));
  });
  document.title = t("app.title");
  document.querySelectorAll(".menu-item[data-view]").forEach((btn) => {
    btn.textContent = t(`nav.${btn.dataset.view}`);
  });
}

function initPreferences() {
  setTheme(getTheme());
  document.documentElement.lang = getLang();
  applyStaticI18n();
}
